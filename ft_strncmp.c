#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t				i;
	const unsigned char	*sc1 = (const unsigned char	*)s1;
	const unsigned char	*sc2 = (const unsigned char	*)s2;

	i = 0;
	while ((sc1[i] != '\0' || sc2[i] != '\0') && i < n)
	{
		if (sc1[i] != sc2[i])
			return (sc1[i] - sc2[i]);
		i++;
	}
	return (0);
}
